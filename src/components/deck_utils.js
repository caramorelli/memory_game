export const levelToVal = (theme, level) => {
  switch(level) {
    case 'easy':
      return 8;
    case 'medium':
      return 12
    case 'hard':
      return 18
    case 'expert':
      return theme === 'lordRings' ? 21 : 22
    default:
      return null;
  }
}

export const deckRetrieval  = (theme) => {
  return allCards[`${theme}`]
}

export const randomCardSelector = (cardCount, allCards) => {
  let selectedCards = []
  let selectedIdx = []

  while ( selectedCards.length < cardCount ) {
    let randomNum = Math.floor(Math.random() * allCards.length)
    if ( !selectedIdx.includes(randomNum) ) {
      selectedCards.push(allCards[randomNum])
      selectedIdx.push(randomNum)
    }
  }
  return selectedCards
}


export const deckBuilder  = ( cardCount, allCards ) => {

  const cards = randomCardSelector(cardCount, allCards)

  const doubleDeck = cards.concat(cards)
  let setupDeck = []
  let takenIndex = []
  cardCount *= 2

  while ( setupDeck.length < cardCount ) {
    let randomNum = Math.floor(Math.random() * cardCount)
    if (!takenIndex.includes(randomNum)) {
      setupDeck.push(doubleDeck[randomNum])
      takenIndex.push(randomNum)
    }
  }
  return setupDeck
}



export const boardSetup = ( theme, level ) => {
  const totalCount = levelToVal(theme, level)
  const allCards = deckRetrieval(theme)
  const currentCards = deckBuilder(totalCount, allCards)

  return { cardQuantity: totalCount, fullDeck: allCards, currentDeck: currentCards }

}
